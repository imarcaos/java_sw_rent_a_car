    public boolean verify(String add_or_edit) {
        
        String fullname = jTextField_fullname.getText();            
        String nif = jTextField_nif.getText();
        String birthdate = datePicker_birthdate.getDate().toString();
        String phone = jTextField_phone.getText();
        String email = jTextField_email.getText();
        String address = jTextArea_address.getText();
        
        boolean val = false;
        
        if(!fullname.trim().equals("") && !nif.trim().equals("") && !phone.trim().equals("") && !email.trim().equals("") && !address.trim().equals("")) {
            val = true;
        } else {
            JOptionPane.showMessageDialog(null, "Digite um valor válido!", "Informação Inválida", 2);
            val = false;
        }
        
        return val;
    }

