    private void jButton_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addActionPerformed
        // Add a new customer
        
        try {
            String fullname = jTextField_fullname.getText();
            int nif = Integer.parseInt(jTextField_nif.getText());
            String birthdate = datePicker_birthdate.getDate().format(DateTimeFormatter.ISO_LOCAL_DATE);
            String phone = jTextField_phone.getText();
            String email = jTextField_email.getText();
            String address = jTextArea_address.getText();

            if(verify("add")) {
                customer.addCustomer(fullname, nif, birthdate, phone, email, address);
            }   
        
            // populate jTable
            reloadJTable();
        
        } catch (Exception ex) {
            System.out.println("Erro addLocation: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton_addActionPerformed

    private void jButton_EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EditActionPerformed
        // Edit a customer
        int id = (int) jSpinner_id.getValue();
        try {
            String fullname = jTextField_fullname.getText();
            int nif = Integer.parseInt(jTextField_nif.getText());
            String birthdate = datePicker_birthdate.getDate().format(DateTimeFormatter.ISO_LOCAL_DATE);
            String phone = jTextField_phone.getText();
            String email = jTextField_email.getText();
            String address = jTextArea_address.getText();
                        
            if(verify("edit")) {
                customer.editCustomer(id, fullname, nif, birthdate, phone, email, address);
                
                // populate jTable
                reloadJTable(); 
            }   
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Edit Customer", 2);
            //System.out.println("Erro ao ler dados: " + ex.getMessage());
        }        
        
    }//GEN-LAST:event_jButton_EditActionPerformed

