    private void jTable_customersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_customersMouseClicked
        // Get selected customer
        int index = jTable_customers.getSelectedRow();
        int id = Integer.valueOf(jTable_customers.getValueAt(index, 0).toString());
        String fullname = jTable_customers.getValueAt(index, 1).toString();
        String nif = jTable_customers.getValueAt(index, 2).toString();
        //Date birthdate = jTable_customers..getValueAt(index, 3);
        String phone = jTable_customers.getValueAt(index, 4).toString();
        String email = jTable_customers.getValueAt(index, 5).toString();
        String address = jTable_customers.getValueAt(index, 6).toString();
        
        jSpinner_id.setValue(id);
        jTextField_fullname.setText(fullname);
        jTextField_nif.setText(nif);
        //datePicker_birthdate.setDate(birthdate);
        jTextField_phone.setText(phone);
        jTextField_email.setText(email);
        jTextArea_address.setText(address);       

        // Space to pick up and check the date
        // To format the date
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");

        // Pick up the date on table
        String dateString = jTable_customers.getValueAt(index, 3).toString();
        // System.out.println(dateString); // until here ok, show the date

        try {
            // Convert the String to LocalDate
            LocalDate bdate = LocalDate.parse(dateString, formatter);

            // Define the date on the date picker
            datePicker_birthdate.setDate(bdate);
//            System.out.println("Data definida: " + dateString); //my check date
            
        } catch (Exception e) {
            System.out.println("Erro ao processar a data: " + e.getMessage());
        }
        // End the Space to pick up and check the date

        
    }//GEN-LAST:event_jTable_customersMouseClicked

